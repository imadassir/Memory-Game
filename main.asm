		MOVWF	P7
		MOVLW	D'13'
		MOVWF	P8
		MOVLW	D'15'
		MOVWF	P9
		MOVLW	D'12'
		MOVWF	P10
		MOVLW	D'10'
		MOVWF	P11
		MOVLW	D'14'
		MOVWF	P12
		MOVLW	D'0'
		MOVWF	STAR
		RETURN

WLCM	CALL	PRINTM
		CALL	PRINTE
		CALL	PRINTM
		CALL	PRINTSP
		CALL	PRINTG
		CALL	PRINTA
		CALL	PRINTM
		CALL	PRINTE
		CALL	DELAY
		CALL	DELAY
		CALL	DELAY
		CALL	CLEAR
		RETURN

MENU	CALL	PRINTM
		CALL	PRINTO
		CALL	PRINTD
		CALL	PRINTE
		CALL	PRINTSP
		CALL	PRINTSP
		CALL	PRINT1
		CALL	PRINTSP
		CALL	PRINTSP
		CALL	PRINT2
		CALL	PRINTSP
		CALL	PRINTSP
		CALL	PRINT3
		MOVLW	B'01000'
		CALL	ET
		MOVLW	B'00101'
		CALL 	ET
		CALL	PRINTST
		RETURN


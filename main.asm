		title   "LCD"
        list    p=16f84A
        radix   hex
        include "p16f84A.inc"
        
COUNT1  EQU    	d'12'
COUNT2	EQU		d'13'
COUNT3	EQU		d'14'
P1		EQU		d'15'
P2		EQU		d'16'
P3		EQU		d'17'
P4		EQU		d'18'
P5		EQU		d'19'
P6		EQU		d'20'
P7		EQU		d'21'
P8		EQU		d'22'
P9		EQU		d'23'
P10		EQU		d'24'
P11		EQU		d'25'
P12		EQU		d'26'
FLAG	EQU		d'27'
STAR	EQU		d'28'

      	ORG    	0x0
		GOTO	START
		ORG		0x04
;		BTFSC	INTCON,INTF		;interrupts
;		GOTO	INTRB0
		BTFSC	INTCON,RBIF
		GOTO	INTRB5
;		BTFSC	INTCON,T0IF
;		GOTO	TIMERZ
		

		;init I/O ports
START	BSF		STATUS,RP0
		CLRF	TRISA		;LCD DB(4,7) = RA(0-3)	//	RS = RA4
		CLRF	TRISB		;LCD ENABLE = RB1	// RED LED = RB2
							;GREEN LED = RB3	//	BUZZER=RB0
		BSF		TRISB,4		;left
		BSF		TRISB,5		;right
		BSF		TRISB,6		;up/down
		BSF		TRISB,7		;confirm
		MOVLW	B'10000111'
		MOVWF	OPTION_REG
		BCF		STATUS,RP0
		CLRF	PORTA
		CLRF	PORTB
		CLRF	INTCON
		BSF		INTCON,RBIE
;		BSF		INTCON,T0IE
		BSF		INTCON,GIE
		CALL	INITPOS
		CALL 	INITROU
		CALL	WLCM
		CALL	MENU
TASK	GOTO 	TASK
	
	;INIT ROUTINE
INITROU CALL	DELAY
		MOVLW	b'00010'
		CALL 	ET
		MOVLW	b'00010'
		CALL 	ET
		MOVLW	b'01000'
		CALL 	ET
	;DISPLAY ON/OFF CONTROL
		MOVLW	b'00000'
		CALL 	ET
		MOVLW	b'01111'
		CALL 	ET
	;CLEAR DISPLAY
		MOVLW	b'00000'
		CALL 	ET
		MOVLW	b'00001'
		CALL 	ET
	;ENTRY MODE SET
		MOVLW	b'00000'
		CALL 	ET
		MOVLW	b'00110'
		CALL 	ET
		NOP
		RETURN

ET		MOVWF	PORTA
		BSF 	PORTB,1
		NOP
		BCF		PORTB,1
		CALL 	DELAY			;change to <40ms to write faster
		RETURN

;interrupt routines
INTRB4	CALL	DELAY1
		BTFSS	PORTB,4
		BSF		PORTB,1
		BTFSC	PORTB,4
		BCF		PORTB,1
		BCF		INTCON,RBIF
		RETFIE

INTRB0	CALL 	DELAY1
		BTFSS	PORTB,0
		CLRF	PORTA
		BCF		INTCON,INTF
		RETFIE

INTRB5	CALL	DELAY1		;DEBOUNCE
		BTFSS	PORTB,5
		GOTO	ACT1
		GOTO	END5


ACT1	BTFSS	STAR,1
		GOTO	ACT2
		CLRF	STAR

		MOVLW	B'01000'	;GO BACK TO ADD:11
		CALL	ET
		MOVLW	B'01011'
		CALL 	ET
		CALL	PRINTSP

		MOVLW	B'01000'	;GO TO ADD:5
		CALL	ET
		MOVLW	B'00101'
		CALL 	ET
		CALL	PRINTST

		GOTO	END5
		

ACT2	INCF	STAR
		BTFSS	STAR,0
		GOTO	ACT3
		MOVLW	B'01000'	;GO BACK TO ADD:5
		CALL	ET
		MOVLW	B'00101'
		CALL 	ET
		CALL	PRINTSP		
		MOVLW	B'01000'	;GO TO ADD:8
		CALL	ET
		MOVLW	B'01000'
		CALL 	ET
		CALL	PRINTST
		GOTO	END5

ACT3	INCF	STAR
		MOVLW	B'01000'	;GO BACK TO ADD:8
		CALL	ET
		MOVLW	B'01000'
		CALL 	ET
		CALL	PRINTSP		
		MOVLW	B'01000'	;GO TO ADD:11
		CALL	ET
		MOVLW	B'01011'
		CALL 	ET
		CALL	PRINTST
		GOTO	END5	


END5	BCF		INTCON,RBIF
		RETFIE
		

TIMERZ	MOVLW	d'16'
		MOVWF	COUNT1
		DECFSZ	COUNT1,F
		GOTO	FINISH
		INCF	PORTA
		CLRF	TMR0
		MOVLW	d'16'
		MOVWF	COUNT1
FINISH	BCF		INTCON,T0IF
		RETFIE

;DELAYS
DELAY   MOVLW   H'0'		;40ms delay
        MOVWF   COUNT1
		MOVLW	d'52'
        MOVWF	COUNT2
LOOP3   INCFSZ	COUNT1,F
      	GOTO    LOOP3
		DECFSZ	COUNT2,F
		GOTO	LOOP3 
		RETURN


DELAY1  MOVLW    H'06'		;1ms delay
        MOVWF    COUNT1
            
LOOP    NOP
		INCFSZ	COUNT1,F
        GOTO    LOOP
        RETURN

INITPOS	MOVLW	D'12'
		MOVWF	P1
		MOVLW	D'10'
		MOVWF	P2
